class No
{
    // atributo privado para o valor do nó
    private int valor;

    // atribuot privado para a referência para o próximo nó
    private No? proximo = null;

    // Métodos get e set para o valor do nó
    public int Valor
    {
        get { return valor; }
        set { valor = value; }
    }

    // Métodos get e set para o valor do nó
    public No? Proximo
    {
        get { return proximo; }
        set { proximo = value; }
    }
}
